    private void limpiarCampos() {
        txtCodigoAnimal.setText(null);
        txtGeneroAnimal.setText(null);
        txtProblema.setText(null);
        txtTipoAnimal.setText(null);
        txtNombre.setText(null);
    }

    private void bloquearCampos() {
        txtCodigoAnimal.setEditable(false);
        txtGeneroAnimal.setEditable(false);
        txtProblema.setEditable(false);
        txtTipoAnimal.setEditable(false);
        txtNombre.setEditable(false);
    }

    private void llenarCampos() {
        try {
            Animal animal = colaAnimales.frente();
            txtCodigoAnimal.setText(animal.getCodigoString());
            txtGeneroAnimal.setText(animal.getGenero());
            txtProblema.setText(animal.getProblemaRemisionVeterinario());
            txtNombre.setText(animal.getNombre());
            if (animal instanceof Cerdo) {
                txtTipoAnimal.setText("Cerdo");
            } else if (animal instanceof Caballo) {
                txtTipoAnimal.setText("Caballo");
            } else {
                txtTipoAnimal.setText("Vacuno");
            }
        } catch (ColaVaciaException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }

    private void validarUsuarioLogeado() {
        Usuario usuarioLogeado = controladorUsuario.getUsuarioLogueado();
        if (usuarioLogeado instanceof Administrador) {
            btnVolver.setVisible(true);
            btnAtenderAnimal.setEnabled(false);
        }
    }

    private void añadirAnimalAtendido() {
        Usuario usuario = vistaLogIn.getControladorUsuario().getUsuarioLogueado();
        if (usuario instanceof Veterinario) {
            ((Veterinario) usuario).añadirAnimalAtendido();
            controladorUsuario.escribirUsuarios();
        }
    }
    
    private void eliminarAnimalAtendido() {
        Usuario usuario = vistaLogIn.getControladorUsuario().getUsuarioLogueado();
        if (usuario instanceof Veterinario) {
            ((Veterinario) usuario).restarAnimalAtendido();
            controladorUsuario.escribirUsuarios();
        }
    }
    

