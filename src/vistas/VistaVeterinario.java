    private void btnAdelanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdelanteActionPerformed
        // TODO add your handling code here:
        if (vistaLogIn.getControladorPila().pilaDosAnimalesIsEmpty() == false) {
            AccionRealizadaAnimal accion = vistaLogIn.getControladorPila().popAccionPilaDosAnimales();
            if (accion.getAccion().equals(AccionRealizadaAnimal.ELIMINAR)) {
                System.out.println("Se elimina");
                Animal animal = colaAnimales.desencolar();
                HistoriaClinica historiaClinica = vistaLogIn.getControladorPila().popPilaHistoriaClinicaDos();
                animal.añadirAlHistorialClinico(historiaClinica);
                añadirAnimalAtendido();
                limpiarCampos();
                llenarCampos();
                vistaLogIn.getControladorPila().pushPilaHistoriaClinicaUno(historiaClinica);
                accion.setAccion(AccionRealizadaAnimal.REGISTRAR);
            }
            vistaLogIn.getControladorPila().pushAccionPilaUnoAnimales(accion);
        } else {
            JOptionPane.showMessageDialog(null, "No se puede adelantar mas");
        }
    }//GEN-LAST:event_btnAdelanteActionPerformed

    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed
        // TODO add your handling code here:
////        System.out.println("Lo que llego de pila dos: " + vistaLogIn.getControladorPila().popPilaHistoriaClinicaUno());
        if (vistaLogIn.getControladorPila().pilaUnoAnimalesIsEmpty() == false) {
            AccionRealizadaAnimal accion = vistaLogIn.getControladorPila().popAccionPilaUnoAnimales();
            HistoriaClinica historiaClinica = null;
            if (accion.getAccion().equals(AccionRealizadaAnimal.REGISTRAR)) {
                Animal animal = accion.getAnimal();
                historiaClinica = vistaLogIn.getControladorPila().popPilaHistoriaClinicaUno();
                animal.eliminarHistorialClinico(historiaClinica);
                colaAnimales.ponerFrente(animal);
                eliminarAnimalAtendido();
                llenarCampos();
                accion.setAccion(AccionRealizadaAnimal.ELIMINAR);
            }
            vistaLogIn.getControladorPila().pushAccionPilaDosAnimales(accion);
            vistaLogIn.getControladorPila().pushPilaHistoriaClinicaDos(historiaClinica);
        } else {
            JOptionPane.showMessageDialog(null, "No se puede retroceder mas.");
        }
    }//GEN-LAST:event_btnAtrasActionPerformed

