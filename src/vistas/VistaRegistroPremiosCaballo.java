    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtPremio.getText().isEmpty() || txtAño.getText().isEmpty()) {
                throw new CompletarCampoVacioException();
            }
            String premio = txtPremio.getText();
            String año = txtAño.getText();
            PremioCaballo premioCaballo = new PremioCaballo(premio, año);
            animal.agregarPremioAlCaballo(premioCaballo);
            AccionRealizadaAnimal accion = new AccionRealizadaAnimal(animal, AccionRealizadaAnimal.REGISTRAR);
            vistaLogIn.getControladorPila().pushAccionPilaUnoAnimales(accion);
            vistaLogIn.getControladorPila().pushPilaPremiosUno(premioCaballo);
            agregarPremioTabla(premioCaballo);
            JOptionPane.showMessageDialog(null, "Se registró el premio del caballo.");
            limpiarCampos();
            vistaLogIn.getControladorPesebrera().escribirPesebreras();
        } catch (CompletarCampoVacioException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

