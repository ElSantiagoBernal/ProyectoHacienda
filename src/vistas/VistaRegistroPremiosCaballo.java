    private void declararColumnasTabla() {
        modeloPremios = new DefaultTableModel();
        modeloPremios.addColumn("Premio");
        modeloPremios.addColumn("Año");
        this.tblPremiosCaballo.setModel(modeloPremios);
    }

    private void agregarPremioTabla(PremioCaballo premio) {
        String[] datos = new String[2];
        datos[0] = premio.getPremio();
        datos[1] = premio.getFecha();
        modeloPremios.addRow(datos);
    }

    private void cargarTabla() {
        for (int i = 0; i < animal.obtenerTamañoListaPremios(); i++) {
            PremioCaballo premio = animal.recorrerListaPremios(i);
            String[] datos = new String[2];
            datos[0] = premio.getPremio();
            datos[1] = premio.getFecha();
            modeloPremios.addRow(datos);
        }
    }

    private void reducirEspacioColumnaFecha() {
        tblPremiosCaballo.getColumnModel().getColumn(1).setMaxWidth(100);
        tblPremiosCaballo.getColumnModel().getColumn(1).setMinWidth(100);
        tblPremiosCaballo.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(100);
        tblPremiosCaballo.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(100);
        tblPremiosCaballo.getTableHeader().getColumnModel().getColumn(1).setMinWidth(100);
    }

    private void eliminarObjetosTabla() {
        modeloPremios.setRowCount(0);
    }

