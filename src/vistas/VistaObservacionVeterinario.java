    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (txtObservacion.getText().isEmpty() || txtObservacion.getText().equals("Escriba aqui, la observación del animal...")) {
                throw new CompletarCampoVacioException();
            }
            String diagnostico = txtObservacion.getText();
            HistoriaClinica historiaClinica = new HistoriaClinica(obtenerFechaActual(), diagnostico);
            animal.añadirAlHistorialClinico(historiaClinica);
            añadirAnimalAtendido();
            AccionRealizadaAnimal accion = new AccionRealizadaAnimal(animal, AccionRealizadaAnimal.REGISTRAR);
            vistaLogIn.getControladorPila().pushAccionPilaUnoAnimales(accion);
            vistaLogIn.getControladorPila().pushPilaHistoriaClinicaUno(historiaClinica);
            JOptionPane.showMessageDialog(null, "Se ha atendido el animal.");
            VistaVeterinario vista = new VistaVeterinario(vistaLogIn);
            vista.setVisible(true);
            this.dispose();
        } catch (CompletarCampoVacioException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

