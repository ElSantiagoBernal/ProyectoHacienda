    public static String obtenerFechaActual() {
        Date fecha = new Date();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/YYYY");
        return formato.format(fecha);
    }

    private void añadirAnimalAtendido() {
        Usuario usuario = vistaLogIn.getControladorUsuario().getUsuarioLogueado();
        if (usuario instanceof Veterinario) {
            ((Veterinario) usuario).añadirAnimalAtendido();
            vistaLogIn.getControladorUsuario().escribirUsuarios();
        }
    }

