    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtId.getText().isEmpty()) {
                throw new CompletarCampoVacioException();
            }
            String id = txtId.getText();
            Usuario usuario = controladorUsuario.buscarUsuarioPorId(id);
            if (usuario == null) {
                throw new UsuarioNoRegistradoException();
            }
            llenarCampos(usuario);
        } catch (CompletarCampoVacioException | UsuarioNoRegistradoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty()
                    || txtEdad.getText().isEmpty()) {
                throw new CompletarCampoVacioException();
            }
            String id = txtId.getText();
            String nuevoNombre = txtNombre.getText();
            String nuevoApellido = txtApellido.getText();
            int nuevaEdad = Integer.parseInt(txtEdad.getText());
            controladorUsuario.editarInfoUsuario(id, nuevoNombre, nuevoApellido, nuevaEdad);
            JOptionPane.showMessageDialog(null, "Se editó la información");
            limpiarCampos();
            bloquearCamposYBotones();
        } catch (CompletarCampoVacioException | UsuarioNoRegistradoException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }


    }//GEN-LAST:event_btnEditarActionPerformed

