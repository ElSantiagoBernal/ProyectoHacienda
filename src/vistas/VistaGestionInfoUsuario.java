    private void limpiarCampos() {
        txtId.setText("Identificación");
        txtNombre.setText("Nombre(s)");
        txtApellido.setText("Apellido(s)");
        txtEdad.setText("Edad");
    }

    private void bloquearCamposYBotones() {
        bloquearBotones();
        bloquearCampos();
    }

    private void desbloquearCamposYBotones() {
        desbloquearBotones();
        desbloquearCampos();
    }

    private void desbloquearCampos() {
        txtNombre.setEditable(true);
        txtApellido.setEditable(true);
        txtEdad.setEditable(true);
        textoOtro.setVisible(true);
        txtOtro.setVisible(true);
        txtUsuarioEncontrado.setVisible(true);
        txtId.setEnabled(false);
    }

    private void bloquearCampos() {
        txtNombre.setEditable(false);
        txtApellido.setEditable(false);
        txtEdad.setEditable(false);
        textoOtro.setVisible(false);
        txtOtro.setVisible(false);
        txtOtro.setEditable(false);
        txtUsuarioEncontrado.setVisible(false);
        txtUsuarioEncontrado.setEditable(false);
        txtId.setEnabled(true);
    }

    private void desbloquearBotones() {
        btnEliminar.setEnabled(true);
        btnEditar.setEnabled(true);
    }

    private void bloquearBotones() {
        btnEliminar.setEnabled(false);
        btnEditar.setEnabled(false);
    }

    private void llenarCampos(Usuario usuario) {
        if (usuario instanceof Administrador) {
            txtUsuarioEncontrado.setText("Administrador");
            textoOtro.setText("Usuarios registrados");
            txtOtro.setText(String.valueOf(((Administrador) usuario).getNumeroUsuariosRegistrados()));
            desbloquearCampos();
            btnEditar.setEnabled(true);
        } else if (usuario instanceof EncargadoCaballo) {
            txtUsuarioEncontrado.setText("Encargado de caballos");
            desbloquearCamposYBotones();
        } else if (usuario instanceof EncargadoGanado) {
            txtUsuarioEncontrado.setText("Encargado del ganado");
            textoOtro.setText("Toros vendidos");
            txtOtro.setText(String.valueOf(((EncargadoGanado) usuario).getNumeroTorosVendidos()));
            desbloquearCamposYBotones();
        } else if (usuario instanceof Veterinario) {
            txtUsuarioEncontrado.setText("Veterinario");
            textoOtro.setText("Animales atentidos");
            txtOtro.setText(String.valueOf(((Veterinario) usuario).getNumeroAnimalesAtendidos()));
            desbloquearCamposYBotones();
        } else {
            txtUsuarioEncontrado.setText("Criador de cerdos");
            textoOtro.setText("Cerdos vendidos");
            desbloquearCamposYBotones();
        }
        txtNombre.setText(usuario.getNombre());
        txtApellido.setText(usuario.getApellido());
        txtEdad.setText(String.valueOf(usuario.getEdad()));
    }

