    private void declararColumnasTabla() {
        modeloCuidados = new DefaultTableModel();
        modeloCuidados.addColumn("Cuidado");
        modeloCuidados.addColumn("Año");
        this.tblCuidadosCaballo.setModel(modeloCuidados);
    }

    private void reducirEspacioColumnaFecha() {
        tblCuidadosCaballo.getColumnModel().getColumn(1).setMaxWidth(100);
        tblCuidadosCaballo.getColumnModel().getColumn(1).setMinWidth(100);
        tblCuidadosCaballo.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(100);
        tblCuidadosCaballo.getTableHeader().getColumnModel().getColumn(1).setMaxWidth(100);
        tblCuidadosCaballo.getTableHeader().getColumnModel().getColumn(1).setMinWidth(100);
    }

    private void cargarTabla() {
        for (int i = 0; i < animal.obtenerTamañoListaCuidados(); i++) {
            CuidadoCaballo cuidado = animal.recorrerListaCuidados(i);
            String[] datos = new String[2];
            datos[0] = cuidado.getCuidado();
            datos[1] = cuidado.getFecha();
            modeloCuidados.addRow(datos);
        }
    }

    private void agregarCuidadoTabla(CuidadoCaballo cuidado) {
        String[] datos = new String[2];
        datos[0] = cuidado.getCuidado();
        datos[1] = cuidado.getFecha();
        modeloCuidados.addRow(datos);
    }

    public static String obtenerFechaActual() {
        Date fecha = new Date();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/YYYY");
        return formato.format(fecha);
    }

    private void eliminarObjetosTabla() {
        modeloCuidados.setRowCount(0);
    }

    private void limpiarCampos() {
        txtCuidado.setText(null);
    }

    private void añadirCuidadoAlEpmpleado() {
        Usuario usuario = vistaLogIn.getControladorUsuario().getUsuarioLogueado();
        if (usuario instanceof EncargadoCaballo) {
            ((EncargadoCaballo) usuario).añadirCuidado();
            vistaLogIn.getControladorUsuario().escribirUsuarios();
        }
    }

    private void eliminarCuidadoAlEmpleado() {
        Usuario usuario = vistaLogIn.getControladorUsuario().getUsuarioLogueado();
        if (usuario instanceof EncargadoCaballo) {
            ((EncargadoCaballo) usuario).restarCuidadoo();
            vistaLogIn.getControladorUsuario().escribirUsuarios();
        }
    }

