    private void btnRegistrarCuidadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarCuidadoActionPerformed
        // TODO add your handling code here:
        try {
            if (txtCuidado.getText().isEmpty()) {
                throw new CompletarCampoVacioException();
            }
            String cuidado = txtCuidado.getText();
            String fecha = obtenerFechaActual();
            CuidadoCaballo cuidadoCaballo = new CuidadoCaballo(cuidado, fecha);
            animal.agregarCuidadoAlCaballo(cuidadoCaballo);
            agregarCuidadoTabla(cuidadoCaballo);
            AccionRealizadaAnimal accion = new AccionRealizadaAnimal(animal, AccionRealizadaAnimal.REGISTRAR);
            vistaLogIn.getControladorPila().pushAccionPilaUnoAnimales(accion);
            vistaLogIn.getControladorPila().pushPilaCuidadosUno(cuidadoCaballo);
            JOptionPane.showMessageDialog(null, "Se registró el cuidado del caballo.");
            limpiarCampos();
            añadirCuidadoAlEpmpleado();
            vistaLogIn.getControladorPesebrera().escribirPesebreras();
        } catch (CompletarCampoVacioException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarCuidadoActionPerformed

